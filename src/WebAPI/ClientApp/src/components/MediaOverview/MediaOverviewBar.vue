<template>
	<q-toolbar class="media-overview-bar">
		<!--	Title	-->
		<q-toolbar-title>
			<QRow
				align="center"
				justify="start">
				<Transition
					appear
					enter-active-class="animated fadeInLeft"
					leave-active-class="animated fadeOutLeft">
					<QCol
						v-if="detailMode"
						cols="auto">
						<q-btn
							flat
							icon="mdi-arrow-left"
							size="xl"
							@click="$emit('back')" />
					</QCol>
				</Transition>
				<QCol cols="auto">
					<q-list class="no-background">
						<q-item>
							<q-item-section avatar>
								<QMediaTypeIcon
									:media-type="mediaOverviewStore.mediaType ?? PlexMediaType.None"
									:size="36"
									class="mx-3" />
							</q-item-section>
							<q-item-section>
								<q-item-label v-if="!mediaOverviewStore.allMediaMode">
									{{ server ? serverStore.getServerName(server.id) : $t('general.commands.unknown') }}
									{{ $t('general.delimiter.dash') }}
									{{ library ? libraryStore.getLibraryName(library.id) : $t('general.commands.unknown') }}
								</q-item-label>
								<q-item-label v-else>
									{{ headerText }}
								</q-item-label>
								<q-item-label
									v-if="library && !detailMode"
									caption>
									{{ libraryCountFormatted }}
									{{ $t('general.delimiter.dash') }}
									<QFileSize :size="library.mediaSize" />
								</q-item-label>
							</q-item-section>
						</q-item>
					</q-list>
				</QCol>
				<QCol align-self="center">
					<q-input
						v-model="mediaOverviewStore.filterQuery"
						:debounce="300"
						outlined
						input-style="font-size: 1.25rem"
						rounded>
						<template #prepend>
							<q-icon
								name="mdi-magnify"
								class="q-ml-sm" />
						</template>
						<template #append>
							<q-icon
								v-if="mediaOverviewStore.filterQuery !== ''"
								name="mdi-close"
								class="cursor-pointer q-mr-sm"
								@click="mediaOverviewStore.filterQuery = ''" />
						</template>
					</q-input>
				</QCol>
			</QRow>
		</q-toolbar-title>

		<!--	Download button	-->
		<VerticalButton
			v-if="mediaOverviewStore.showDownloadButton"
			:height="barHeight"
			:label="$t('general.commands.download')"
			:width="verticalButtonWidth"
			icon="mdi-download"
			@click="download" />

		<!--	Selection Dialog Button	-->
		<VerticalButton
			v-if="mediaOverviewStore.showSelectionButton"
			:height="barHeight"
			:label="$t('general.commands.selection')"
			:width="verticalButtonWidth"
			icon="mdi-select-marker"
			@click="$emit('selection-dialog')" />

		<!--	Refresh library button	-->
		<VerticalButton
			v-if="!mediaOverviewStore.allMediaMode && !detailMode"
			:height="barHeight"
			:label="$t('general.commands.refresh')"
			:width="verticalButtonWidth"
			cy="media-overview-refresh-library-btn"
			icon="mdi-refresh"
			@click="refreshLibrary" />

		<!--	View mode	-->
		<VerticalButton
			v-if="!detailMode"
			:height="barHeight"
			:label="$t('general.commands.view')"
			:width="verticalButtonWidth"
			cy="change-view-mode-btn"
			icon="mdi-eye">
			<q-menu
				anchor="bottom left"
				auto-close
				self="top left">
				<q-list>
					<q-item
						v-for="(viewOption, i) in viewOptions"
						:key="i"
						:data-cy="`view-mode-${viewOption.viewMode.toLowerCase()}-btn`"
						clickable
						style="min-width: 200px"
						@click="changeView(viewOption.viewMode)">
						<!-- View mode options -->
						<q-item-section avatar>
							<q-avatar>
								<q-icon
									v-if="isSelected(viewOption.viewMode)"
									name="mdi-check" />
							</q-avatar>
						</q-item-section>
						<!--	Is selected icon	-->
						<q-item-section> {{ viewOption.label }}</q-item-section>
					</q-item>
				</q-list>
			</q-menu>
		</VerticalButton>
	</q-toolbar>
</template>

<script lang="ts" setup>
import type { PlexLibraryDTO, PlexServerDTO } from '@dto';
import { PlexMediaType, ViewMode } from '@dto';
import {
	useLibraryStore,
	useMediaOverviewBarDownloadCommandBus,
	useMediaOverviewStore,
	useServerStore,
	useI18n,
} from '#imports';

const libraryStore = useLibraryStore();
const serverStore = useServerStore();
const mediaOverviewStore = useMediaOverviewStore();
const downloadCommandBus = useMediaOverviewBarDownloadCommandBus();

interface IViewOptions {
	label: string;
	viewMode: ViewMode;
}

const { t } = useI18n();

const props = defineProps<{
	server: PlexServerDTO | null;
	library: PlexLibraryDTO | null;
	detailMode?: boolean;
}>();

const emit = defineEmits<{
	(e: 'back' | 'selection-dialog'): void;
	(e: 'refresh-library', libraryId: number): void;
	(e: 'view-change', viewMode: ViewMode): void;
}>();

const barHeight = ref(85);
const verticalButtonWidth = ref(120);

const refreshLibrary = () => {
	emit('refresh-library', props.library?.id ?? -1);
};

const download = () => {
	downloadCommandBus.emit('download');
};

const changeView = (viewMode: ViewMode) => {
	emit('view-change', viewMode);
};

const isSelected = (viewMode: ViewMode) => {
	return mediaOverviewStore.getMediaViewMode === viewMode;
};

const libraryCountFormatted = computed(() => {
	if (mediaOverviewStore.library) {
		switch (mediaOverviewStore.mediaType) {
			case PlexMediaType.Movie:
				return `${mediaOverviewStore.library.count} Movies`;
			case PlexMediaType.TvShow:
				return `${mediaOverviewStore.library.count} TvShows - ${mediaOverviewStore.library.seasonCount} Seasons - ${mediaOverviewStore.library.episodeCount} Episodes`;
			default:
				return `Library type ${mediaOverviewStore.mediaType} is not supported in the media count`;
		}
	}
	return 'unknown media count';
});

const viewOptions = computed((): IViewOptions[] => {
	return [
		{
			label: 'Poster View',
			viewMode: ViewMode.Poster,
		},
		{
			label: 'Table View',
			viewMode: ViewMode.Table,
		},
	];
});

const headerText = computed(() => {
	switch (mediaOverviewStore.mediaType) {
		case PlexMediaType.Movie:
			return t('components.media-overview-bar.all-media-mode.movies');
		case PlexMediaType.TvShow:
			return t('components.media-overview-bar.all-media-mode.tv-shows');
		default:
			return `Library type ${mediaOverviewStore.mediaType} is not supported`;
	}
});
</script>

